<h2>Tied votes</h2>
<p>In the unlikely event of a tie at the end of each Stage:</p>
<p><strong>Stage 1 (Knockouts):</strong> <b>more than ten Acts may advance to Stage 2</b> as a result.</p>
<p><strong>Stage 2 (Final):</strong> while <b>more than three Runners-Up may be declared</b>, it is our intention to announce <b>just
        one Grand Winner</b>. The final decision will be determined by an independent panel.</p>

<h2>No votes</h2>
<p>If no votes are cast in a single Round, the winning Act(s) will be determined by an independent panel.</p>
