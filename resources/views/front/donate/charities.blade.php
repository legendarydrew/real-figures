<p>The Contest is about more than creativity: it’s about making a <em>positive impact</em>. The charities supported here
    have been carefully chosen for their vital work in tackling bullying and improving mental health, issues that affect
    millions of people every day.</p>

<div class="donate-charities">
    <p>
        <a href="https://www.kidscape.org.uk/" target="_blank" rel="external">Kidscape</a><br/>
        Kidscape is dedicated to preventing bullying and protecting young people. They provide practical support,
        resources,
        and training to help children and families build confidence and stay safe.</p>

    <p>
        <a href="https://www.mind.org.uk/" target="_blank" rel="external">Mind</a><br/>
        Mind works to ensure that no one faces a mental health problem alone. Through advice, support services, and
        campaigning, they help people understand and manage their mental wellbeing.</p>

    <p>
        <a href="https://www.thecalmzone.net/" target="_blank" rel="external">CALM (Campaign Against Living
            Miserably)</a><br/>
        CALM focuses on preventing suicide, particularly among young men. They offer a free, confidential helpline and
        promote open conversations around mental health.</p>

    <p>
        <a href="https://www.samaritans.org/" target="_blank" rel="external">Samaritans</a><br/>
        Samaritans provide 24/7 emotional support to anyone struggling to cope. Their volunteers are there to listen
        without
        judgment, offering a safe space during difficult moments.</p>

    <p>
        <a href="https://uk.movember.com/" target="_blank" rel="external">Movember</a><br/>
        Movember raises awareness of men’s health issues, including mental health, suicide prevention, and prostate and
        testicular cancer, encouraging people to take action and support one another.</p>
</div>

<p>Every donation helps support these organisations in continuing their essential work. Thank you for being part of
    something that aims to make a real difference.</p>
