<p>An overview of the major terms used throughout this site:</p>

<dl class="rules-terms">
    <dt>Act</dt>
    <dd>A musical artist or group officially signed with CATAWOL Records.
    </dd>
    <dt>Contest</dt>
    <dd>The overall event organised by CATAWOL Records, involving the submission of Songs to raise awareness of
        bullying in adult and hobby spaces.
    </dd>
    <dt>Song</dt>
    <dd>A musical submission created by an Act. The first version of the Song is submitted for Stage 1,
        with an updated version submitted for Stage 2.
    </dd>
    <dt>Stage</dt>
    <dd>A major phase of the Contest, involving one or more Rounds, where winning and runner-up Acts are determined
        through voting.
    </dd>
    <dt>Round</dt>
    <dd>A voting bracket within a Stage.</dd>
    <dt>Visitor</dt>
    <dd>Someone visiting this site who is eligible to cast votes, whether or not they take part in the voting process.</dd>
</dl>
