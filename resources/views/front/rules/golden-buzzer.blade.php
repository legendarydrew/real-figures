<p class="rules-large">Along with voting for their favourite Songs, Visitors also have the option of supporting their
    favourite Acts by awarding them with a <b>Golden Buzzer</b>!</p>

<p>Even if your favourite Act or Song doesn't make it through, awarding them a Golden Buzzer will give them
    <b>the same bonuses as for the runners-up</b> in the current Stage.</p>

<p>Golden Buzzers are awarded by <b>making a donation</b>, wherever you see the star symbol (<i
        class="fa-solid fa-star"></i>) next to an Act's Song.</p>

<h2>Awarding Golden Buzzers</h2>
<p>Golden Buzzers can only be awarded to <b>Acts in the current Stage</b>.</p>
<p><strong>During Stage 1 (Knockouts):</strong> Acts will be given a profile and an updated picture on the <a
        href="{{route('acts')}}">Acts
        page</a>. <em>They might also be asked to submit a final version of their Song, at the discretion of the Contest organisers.</em></p>
<p><strong>During Stage 2 (Final):</strong> Acts will be immortalised as a <b>3D-printed figure</b> in SilentMode's style.</p>

<p class="rules-note">Golden Buzzers have <b>no effect on scores</b> &mdash; they're a way for Visitors to
    support their favourite Acts and Songs, whether they win or not.</p>
