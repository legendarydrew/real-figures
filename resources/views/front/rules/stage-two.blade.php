<p class="rules-large">This second and final Stage will have <b>just one Round</b>, where <b>all qualifying Acts</b> go
    head-to-head to determine which Song will become the official anthem of the Contest.</p>

<h2>Resubmission</h2>
<ul>
    <li>All qualifying Acts will be asked to submit an <b>updated and final version</b> of their Song.</li>
    <li>The new version of each Act's Song must now include a <b>previously undisclosed second verse</b> from
        SilentMode's lyrics.
    </li>
    <li>The new version of the Song must <b>retain the original submission's style and essence</b>, while allowing
        for creative reinterpretation.
    </li>
    <li>There is <b>no maximum duration</b> for the new version of the Song.</li>
</ul>

<h2>Voting</h2>
<p>Voting conditions are exactly the same as for Stage 1, except Visitors will be asked to vote for their
    <b>top 3 favourites</b> from <b>all</b> entries.</p>
<p>Only votes cast while the Round is open will be counted.</p>

<h2>Winning Songs</h2>
<p>At the end of the Stage, <b>one Grand Winner</b> and <b>three Runners-Up</b> will be crowned.</p>
<p>The winning Acts will be revealed to the public after the votes have been counted and confirmed.</p>

<h2>Prizes</h2>
<p>The Grand Winner and Runners-Up will be recreated as <b>3D-printed figures</b> in SilentMode's style.</p>
<p>The Grand Winner will also be honoured by being created as a <b>custom LEGO minifigure</b>.</p>
<p>Finally, the Grand Winner's Song will become the <b>official anthem</b> of the Contest.</p>
