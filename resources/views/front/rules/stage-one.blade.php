<p class="rules-large">This Stage will divide the 32 participating Acts into <b>eight Rounds</b>, each with four Acts
    competing head-to-head.</p>

<h2>Submissions</h2>
<ul>
    <li>Each Act submits a draft version of their Song, according to the Song Criteria.</li>
    <li>For this Stage, the Song length must not exceed <b>2 minutes and 10 seconds</b> (2:10).</li>
</ul>

<h2>Voting</h2>
<p>After the Stage officially begins, each Round runs consecutively for a limited length of time.</p>
<p>During each Round, Visitors can vote for their <b>top three favourite</b> Songs.</p>
<p><b>Voting is open to all Visitors</b>, except for:</p>
<ul>
    <li>members of the F.O.L.D.;</li>
    <li>relatives or affiliates of competing Acts;</li>
    <li>Contest administrators;</li>
    <li>employees of CATAWOL Records.</li>
</ul>
<p>Only votes cast while a Round is open will be counted.</p>

<h2>Advancement</h2>
<p>At the end of the Stage, when all Rounds have been completed, <b>ten</b> Acts will advance to Stage 2:</p>
<ul>
    <li>the <b>winners of each Round</b> (for a total of eight);</li>
    <li>The <b>two highest-scoring runners-up</b> across all Rounds.</li>
</ul>
<p>The winning Acts will be revealed to the public at the beginning of Stage 2.</p>

<h2>Bonus for advancing Acts</h2>
<p>Each Act's entry on the <a href="{{route('acts')}}">Acts page</a> will be updated with a new picture and a profile.
</p>
